#!/bin/perl

#
#順番通りにハッシュから取り出す
#

use Tie::IxHash;

#---------------------------------------#
#準備する
#---------------------------------------#
#-- ハッシュとの関連づけ --#
tie %hash, 'Tie::IxHash';

#-- ハッシュに値を代入 --#
%hash = (
      'a' => 1
	, 'b' => 2
	, 'c' => 3
	, 'd' => 4
	, 'e' => 5
);


#---------------------------------------#
#表示
#---------------------------------------#
foreach my $key ( keys %hash ){
    print "$key,$hash{$key}\n";
}

